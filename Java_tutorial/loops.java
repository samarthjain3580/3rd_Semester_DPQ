package Java_tutorial;

public class loops {
    public static void main(String[] args) {
        //for loop

        for (int i=1; i<=100; i++) {
            System.out.println(i);
        }

        //while loop
        int j=100;
        while (j>=1) {
            System.out.println(j);
            j--;
        }

        //do-while loop
        int k=100;
        do {
            System.out.println(k);
            k--;
        } while (k>=10);

        
}
    
}
